public class Bank_Account {
    String name;
    String address;
    long mobile_Number;
    String email_Id;
    long account_Number;
    int balance;
    public Bank_Account(String name, String address, long mobile_Number, String email_Id,long account_Number,int balance) {
        this.name = name;
        this.address = address;
        this.mobile_Number = mobile_Number;
        this.email_Id = email_Id;
        this.account_Number = account_Number;
        this.balance = balance;
    }
}
